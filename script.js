const yearElement = document.querySelector("#year");
const revealElements = document.querySelectorAll(".reveal");
const languageButtons = document.querySelectorAll("[data-language]");
const metaDescription = document.querySelector('meta[name="description"]');

const getInitialLanguage = () => {
  const savedLanguage = localStorage.getItem("front-language");
  return savedLanguage && translations[savedLanguage] ? savedLanguage : "en";
};

const setLanguage = (language) => {
  const dictionary = translations[language] || translations.en;
  const page = document.body.dataset.page;
  const titleKey = page ? `${page}PageTitle` : "pageTitle";
  const descriptionKey = page ? `${page}MetaDescription` : "metaDescription";

  document.documentElement.lang = language;
  document.title = dictionary[titleKey] || dictionary.pageTitle;

  if (metaDescription) {
    metaDescription.setAttribute("content", dictionary[descriptionKey] || dictionary.metaDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const key = element.dataset.i18nHtml;
    if (dictionary[key]) {
      element.innerHTML = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    element.dataset.i18nAttr.split(",").forEach((pair) => {
      const [attribute, key] = pair.split(":");
      if (attribute && key && dictionary[key]) {
        element.setAttribute(attribute.trim(), dictionary[key]);
      }
    });
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("front-language", language);
};

if (yearElement) {
  yearElement.textContent = new Date().getFullYear();
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.language));
});

setLanguage(getInitialLanguage());

if ("IntersectionObserver" in window) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.16,
      rootMargin: "0px 0px -40px 0px",
    }
  );

  revealElements.forEach((element) => observer.observe(element));
} else {
  revealElements.forEach((element) => element.classList.add("is-visible"));
}
